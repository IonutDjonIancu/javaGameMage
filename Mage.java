package org.gameloft.second;

public class Mage
{
    String mageName;
    double mageHp;
    char mageAttack;
    double mageDmg;
    int mageLvl;

    Mage( String varMageName, double varMageHp, char varMageAttack, double varMageDmg, int varMageLvl )
    {
        this.mageName = varMageName;
        this.mageHp = varMageHp;
        this.mageAttack = varMageAttack;
        this.mageDmg = varMageDmg;
        this.mageLvl = varMageLvl;
    }



}
